package com.heliosapm.jmx.util.helpers;

/**
 * <p>Title: Reconnectable</p>
 * <p>Description: Defines a class that may fail initial connection, or become disconnected and can be periodically reconnected by the reconnector service</p> 
 * <p>Company: Helios Development Group LLC</p>
 * @author Whitehead (nwhitehead AT heliosdev DOT org)
 * <p><code>com.heliosapm.jmx.util.helpers.Reconnectable</code></p>
 */

public interface Reconnectable extends Runnable {
	/**
	 * Attempts a reconnect
	 * @return true if reconnect was successful, false otherwise
	 */
	public boolean reconnect();
	
	/**
	 * Returns a unique identifier for the reconnectable
	 * @return a unique identifier for the reconnectable
	 */
	public String getId();
		
}
